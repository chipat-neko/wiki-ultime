/**
 * Calculer le profit d'une commodité
 * @param {Object} commodity - Commodité
 * @param {string} priceType - 'avg', 'min', 'max'
 * @returns {number} Profit par SCU
 */
export function getCommodityProfit(commodity, priceType = 'avg') {
  return commodity.sellPrice[priceType] - commodity.buyPrice[priceType];
}

/**
 * Trier les commodités par profit
 * @param {Array} commodities - Liste de commodités
 * @param {string} priceType - Type de prix
 * @returns {Array} Commodités triées
 */
export function sortByProfit(commodities, priceType = 'avg') {
  return [...commodities].sort((a, b) => getCommodityProfit(b, priceType) - getCommodityProfit(a, priceType));
}

