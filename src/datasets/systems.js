/**
 * Obtenir tous les emplacements d'atterrissage d'un système
 */
export function getLandingZones(systemId) {
  const system = SYSTEMS_BY_ID[systemId];
  if (!system) return [];
  const zones = [];
  (system.stations || []).forEach(s => {
    if (s.landingZone) zones.push({ ...s, systemId });
  });
  (system.bodies || []).forEach(body => {
    (body.locations || []).forEach(loc => {
      if (loc.landingZone) zones.push({ ...loc, bodyId: body.id, systemId });
    });
    (body.moons || []).forEach(moon => {
      (moon.locations || []).forEach(loc => {
        if (loc.landingZone) zones.push({ ...loc, bodyId: moon.id, parentBodyId: body.id, systemId });
      });
    });
  });
  return zones;
}

