// ─── HELPERS ─────────────────────────────────────────────────────────────────

/** Returns all bodies where a given mineral can be found, with methods */
export function findMineralLocations(mineralId) {
  const results = [];
  for (const body of MINING_BODIES) {
    const methods = [];
    if (body.ship.includes(mineralId)) methods.push('ship');
    if (body.roc.includes(mineralId))  methods.push('roc');
    if (body.hand.includes(mineralId)) methods.push('hand');
    if (methods.length > 0) {
      results.push({ body, methods });
    }
  }
  return results;
}

/** Returns all minerals and harvestables for a given body */
export function getBodyResources(bodyId) {
  return MINING_BODIES.find(b => b.id === bodyId) || null;
}

/** Returns all minerals sorted by value descending */
export function getMineralsSorted() {
  return Object.values(MINERALS).sort((a, b) => b.value - a.value);
}

