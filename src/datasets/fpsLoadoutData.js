/**
 * Calcule les statistiques d'un loadout
 * @param {string|null} weaponId
 * @param {string|null} armorId
 * @param {string[]} gadgetIds
 * @returns {{ totalWeight, dps, mobility, survivability, price, resistances }}
 */
export function calcLoadoutStats(weaponId, armorId, gadgetIds = []) {
  const weapon = LOADOUT_WEAPONS.find(w => w.id === weaponId) || null;
  const armor = LOADOUT_ARMOR_SETS.find(a => a.id === armorId) || null;
  const gadgets = gadgetIds.map(id => LOADOUT_GADGETS.find(g => g.id === id)).filter(Boolean);

  const weaponWeight = weapon ? weapon.weight : 0;
  const armorWeight = armor ? armor.weight : 0;
  const gadgetsWeight = gadgets.reduce((sum, g) => sum + g.weight, 0);
  const totalWeight = parseFloat((weaponWeight + armorWeight + gadgetsWeight).toFixed(1));

  const dps = weapon ? weapon.dps : 0;

  // Mobilité : basée sur l'armure (10 = max), pénalisée par le poids
  const baseMobility = armor ? armor.mobility : 10;
  const weightPenalty = Math.min(3, Math.floor(totalWeight / 10));
  const mobility = Math.max(1, baseMobility - weightPenalty);

  // Survivabilité : basée sur les résistances de l'armure
  let survivability = 1;
  if (armor) {
    const res = armor.totalResistance;
    const totalRes = res.physical + res.energy + res.distortion + res.biochemical + res.stun;
    survivability = Math.min(10, Math.round(totalRes / 60));
  }

  const weaponPrice = weapon ? weapon.price : 0;
  const armorPrice = armor ? armor.price : 0;
  const gadgetsPrice = gadgets.reduce((sum, g) => sum + g.price, 0);
  const price = weaponPrice + armorPrice + gadgetsPrice;

  const resistances = armor ? armor.totalResistance : { physical: 0, energy: 0, distortion: 0, biochemical: 0, stun: 0 };

  return { totalWeight, dps, mobility, survivability, price, resistances };
}

/**
 * Calcule un score global de loadout (0-100)
 * @param {{ dps, mobility, survivability, totalWeight }} stats
 * @returns {number}
 */
export function getLoadoutScore(stats) {
  if (!stats) return 0;
  const { dps, mobility, survivability } = stats;
  // Normalisation : DPS max ~500, mobility max 10, survivability max 10
  const dpsScore = Math.min(40, Math.round((dps / 500) * 40));
  const mobilityScore = Math.min(30, Math.round((mobility / 10) * 30));
  const survScore = Math.min(30, Math.round((survivability / 10) * 30));
  return dpsScore + mobilityScore + survScore;
}

